            $("#register").on("submit", async function(e) {
            
            e.preventDefault();
            var fd = new FormData;
            fd.append('first_name', $("#first_name").val());
            fd.append('last_name', $("#last_name").val());
            fd.append('email', $("#email").val());
          
            console.log(fd)
            $.ajax({
                type: 'POST',
                url: "{{route('subscribe')}}",
                data: fd,
                cache: false,
                contentType: false,
                processData: false,
                success: function($data) {
                    console.log('the data', $data)
                    Swal.close()
                    Swal.fire('Congratulations', 'You have successfully register, please check your email to proceed!', 'success')
                    // window.location.reload()
                    
                },
                error: function(data) {
                    console.log(data)
